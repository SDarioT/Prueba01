    public void actualizarTabla(){
        String[] encabezado = new String[4];
        encabezado[0] = "Codigo";
        encabezado[1] = "Nombre";
        encabezado[2] = "Canton";
        encabezado[3] = "Year";
        
        var datos = new Object[parroquiaControl.listar().size()][4];
        var i =0;
        for(Parroquia parroquia:parroquiaControl.listar()){
            datos[i][0]=parroquia.getCodigo();
            datos[i][1]=parroquia.getNombre();
            datos[i][2]=parroquia.getCanton();
            datos[i][3]=parroquia.getYear();
            i++;
        }
        
        TableModel modeloTabla = new DefaultTableModel(datos, encabezado);
        jTable1.setModel(modeloTabla);

               
    }
